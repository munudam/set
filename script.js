function searchWord() {
  const input = document.getElementById("searchInput").value.toLowerCase().trim();
  const resultDiv = document.getElementById("result");

  if (!input) {
    const allWords = Object.keys(dictionary);
    const firstThreeWords = allWords.slice(0, 3);
    
    let html = '';
    firstThreeWords.forEach(word => {
      html += `
        <div class="result-card">
          <div class="word-title">
            <span>${word}</span>
          </div>
          <div class="word-meaning">${dictionary[word]}</div>
        </div>
      `;
    });
    
    resultDiv.innerHTML = html;
    return;
  }

  if (dictionary[input]) {
    resultDiv.innerHTML = `
      <div class="result-card">
        <div class="word-title">
          <span>${input}</span>
        </div>
        <div class="word-meaning">${dictionary[input]}</div>
      </div>
    `;
  } else {
    resultDiv.innerHTML = `<div class="error-message">❌ Word not found. Try another one!</div>`;
  }
}

function handleKeyPress(event) {
  if (event.key === "Enter") {
    searchWord();
  }
}

// Load first word when page loads
document.addEventListener("DOMContentLoaded", function() {
  searchWord();
});
